#!/usr/bin/ruby   

print "Enter your day:"

day = gets.chomp

case day
when "Tuesday"
    puts "Study Maths"
when "Wednesday"
    puts "Wear Red or Orange"
when "Thursday"
    puts "Study English"
when "Friday"
    puts "Study Commerce"
when "Saturday"
    puts "Enjoy your day"
when "Sunday"
    puts "Stay Home Do study"
end